public class BloodData {
    private String bloodType;
    private String RH;
	//--------------------------------------------
    public void setBloodType(String bloodType) {
        this.bloodType = bloodType;
    }
	//--------------------------------------------
    public void setRH(String RH) {
        this.RH = RH;
    }
	//--------------------------------------------
    public String getBloodType() {
        return bloodType;
    }
	//--------------------------------------------
    public String getRH() {
        return RH;
    }
	//--------------------------------------------
}
